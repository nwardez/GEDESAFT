package filrouge.gedesaft.model;

public class ButtonModel {
	
	Long id;
	String name;
	
	public ButtonModel () {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
